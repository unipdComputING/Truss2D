classdef GLOBAL
  %------------------------------------------------------------------------
  properties(Constant)
    NDOF = 2; %Ux,Uy
  end
  %------------------------------------------------------------------------
  methods
    %----------------------------------------------------------------------
    %costruttore
    function this=GLOBAL
    end
    %----------------------------------------------------------------------
    %distance between two points X1, X2 in a plane
    %$$L=\sqrt{(x_2-x_1)^2+(y_2-y_1)^2}$$
    function [L]=getDistance(this,X1,X2)
        L = sqrt((X2(1)-X1(1))^2+(X2(2)-X1(2))^2);
    end
    %----------------------------------------------------------------------
    %definisce l'angolo di inclinazione tra 2 punti X1,X2 rispetto 
    %l'orizzontale
    %$$\Delta x=x_2-x_1$$
    %$$\Delta y=y_2-y_1$$
    %$$\alpha=tan^{-1}{\frac{\Delta y}{\Delta x}}$$
    function [alpha]=angolo(this,X1,X2)
        dX    = X2(1)-X1(1);
        dY    = X2(2)-X1(2);
        alpha = atan(dY/dX);
    end
    %----------------------------------------------------------------------
    function [R]=getRotationalMatrix(this,alpha)
      c = cos(alpha);
      s = sin(alpha);
      R = [ c   -s 0.0 0.0;
            s    c 0.0 0.0;
           0.0 0.0  c  -s ;
           0.0 0.0  s   c ;];
    end
    %----------------------------------------------------------------------
    function [alpha]=getGlobalBeamAngle(this,X1,X2)
      dX    = X2(1)-X1(1);
      dY    = X2(2)-X1(2);
      L     = sqrt(dY^2+dX^2);
      if dY==0
          alpha = 0.0;
      else
          alpha = 2*atan((L-dX)/dY);
      end
    end
    %----------------------------------------------------------------------
    %----------------------------------------------------------------------
    %----------------------------------------------------------------------
    %----------------------------------------------------------------------
    %----------------------------------------------------------------------
    %----------------------------------------------------------------------
    %----------------------------------------------------------------------
  end
  %------------------------------------------------------------------------
end
